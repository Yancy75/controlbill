    public function activarPTO()
    {
        $employee_obj = new Employeeset();
        $today = Carbon::now()->format('Y-m-d');
        $info = $employee_obj::where('pto_status', '=', 'inactive')
                ->where('pto_date_activation','<=', $today)
                ->get();

        if($info->isNotEmpty())
        {
            foreach($info as $inf)
            {
                $inf->pto_status = 'active';
                $inf->save();
            }
        }
    }
